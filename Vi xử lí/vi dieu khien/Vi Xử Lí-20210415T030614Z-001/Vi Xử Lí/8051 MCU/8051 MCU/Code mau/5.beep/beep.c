/*******************************************************************************
* 实验名	     : 蜂鸣器实验
* 使用的IO	     : P2^1
* 实验效果       : 蜂鸣器响。
* 注意			 ：无源蜂鸣器，需要给脉冲信号才会响
*******************************************************************************/

#include <reg51.h>
sbit Beep =  P2^1 ;
void Delay(unsigned int i) ;
/*******************************************************************************
* 函数名         : main
* 函数功能		   : 主函数
* 输入           : 无
* 输出         	 : 无
*******************************************************************************/

void main()
{
	Beep= 1;	   //给高电平
	Delay(5);	   //延时
	Beep= 0;	   //给低电平
	Delay(5);	   //延时
} 
/*******************************************************************************
* 函数名         : Delay()
* 函数功能		 : 延时函数
* 输入           : 无
* 输出         	 : 无
*******************************************************************************/

void Delay(unsigned int i)
{
	char j;
	for(i; i > 0; i--)
		for(j = 200; j > 0; j--);
}

